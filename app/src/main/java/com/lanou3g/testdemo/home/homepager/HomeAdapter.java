public class HomeAdapter extends FragmentPagerAdapter{

    ArrayList<String> mStringArrayList = new ArrayList<>();
    ArrayList<Fragment> mFragmentArrayList = new ArrayList<>();

    public HomeAdapter(FragmentManager fm, ArrayList<Fragment> fragmentArrayList, ArrayList<String> stringArrayList) {
        super(fm);
        mFragmentArrayList = fragmentArrayList;
        mStringArrayList = stringArrayList;
    }

    @Override
    public Fragment getItem(int position) {
        return mFragmentArrayList.get(position);
    }

    @Override
    public int getCount() {
        return mFragmentArrayList.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return mStringArrayList.get(position);
    }
}
